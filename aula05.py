"""
+, -, *, /, //, **, % retorna o módulo, ()
"""
print(10 * 10)  # multiplicação 
print(10 + 10)  # adição
print(10 - 5)   # Subtração
print(10 / 2)   # divisão
print(10.5 // 3)  # divisão inteira
print(2 ** 10)  # elevação
print(10 % 5) # o resto da divisão
print((25 + 2) * 10 + (45 / 5 ))